package com.stx.Model;

public class CashierModel {

	private int CaiId;
	private String CaiName;
	private int CaiPrice;
	
	public int getCaiId() {
		return CaiId;
	}
	public void setCaiId(int caiId) {
		CaiId = caiId;
	}
	public String getCaiName() {
		return CaiName;
	}
	public void setCaiName(String caiName) {
		CaiName = caiName;
	}
	public int getCaiPrice() {
		return CaiPrice;
	}
	public void setCaiPrice(int caiPrice) {
		CaiPrice = caiPrice;
	}
	
	
}
