package com.stx.Model;

public class RegisteredModel {
	
	private int workerid;
	private String WorkerName;
	private String WorkerPassword;
	private String WorkerSex;
	private String WorkerData;
	private String WorkerPhoneNumber;
	private String workerright;
	private String Status;
	private int wage;
	
	public int getWorkerid() {
		return workerid;
	}
	public void setWorkerid(int workerid) {
		this.workerid = workerid;
	}
	public String getWorkerName() {
		return WorkerName;
	}
	public void setWorkerName(String workerName) {
		WorkerName = workerName;
	}
	public String getWorkerPassword() {
		return WorkerPassword;
	}
	public void setWorkerPassword(String workerPassword) {
		WorkerPassword = workerPassword;
	}
	public String getWorkerSex() {
		return WorkerSex;
	}
	public void setWorkerSex(String workerSex) {
		WorkerSex = workerSex;
	}
	public String getWorkerData() {
		return WorkerData;
	}
	public void setWorkerData(String workerData) {
		WorkerData = workerData;
	}
	public String getWorkerPhoneNumber() {
		return WorkerPhoneNumber;
	}
	public void setWorkerPhoneNumber(String workerPhoneNumber) {
		WorkerPhoneNumber = workerPhoneNumber;
	}
	public String getWorkerright() {
		return workerright;
	}
	public void setWorkerright(String workerright) {
		this.workerright = workerright;
	}
	public String getStatus() {
		return Status;
	}
	public void setStatus(String status) {
		Status = status;
	}
	public int getWage() {
		return wage;
	}
	public void setWage(int wage) {
		this.wage = wage;
	}
}
